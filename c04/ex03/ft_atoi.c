int	ft_atoi(char *str)
{
	int	i;
	int	pn;
	int	res;

	i = 0;
	pn = 1;
	res = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ' )
		i++;
	while (str[i] == '+' || str[i] == '-' )
	{
		if (str[i] == '-')
			pn *= -1; 
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = (str[i] - 48) + (res * 10);
		i++;
	}
	return (res * pn);
}

